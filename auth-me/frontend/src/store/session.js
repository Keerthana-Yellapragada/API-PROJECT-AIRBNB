// frontend/src/store/session.js
import {
    csrfFetch
} from './csrf';

// *****************************************************************************
//****************************** ACTION CREATORS *****************************
const SET_USER = 'session/setUser';
const REMOVE_USER = 'session/removeUser';

const setUser = (user) => {
    return {
        type: SET_USER,
        payload: user,
    };
};

const removeUser = () => {
    return {
        type: REMOVE_USER,
    };
};


// *****************************************************************************
//************************************ THUNKS **********************************
// SIGNUP THUNK:
export const signup = (user) => async (dispatch) => {
    const {
        username,
        email,
        password
    } = user;
    const response = await csrfFetch("/api/users", {
        method: "POST",
        body: JSON.stringify({
            username,
            email,
            password,
        }),
    });
    const data = await response.json();
    dispatch(setUser(data.user));
    return response;
};


//LOGIN THUNK:
export const login = (user) => async (dispatch) => {
    const {
        credential,
        password
    } = user;
    const response = await csrfFetch('/api/session', {
        method: 'POST',
        body: JSON.stringify({
            credential,
            password,
        }),
    });
    const data = await response.json();
    dispatch(setUser(data));
    return response;
};


//our initial state before login
const initialState = {
    user: null
};

// RESTORE USER THUNK:
export const restoreUser = () => async dispatch => {
    const response = await csrfFetch('/api/session');
    const data = await response.json();
    dispatch(setUser(data.user));
    return response;
};


// *****************************************************************************
// ******************************* REDUCERS ********************************



const sessionReducer = (state = initialState, action) => {
    let newState;
    switch (action.type) {
        case SET_USER:
            newState = Object.assign({}, state);
            newState.user = action.payload;
            return newState;
        case REMOVE_USER:
            newState = Object.assign({}, state);
            newState.user = null;
            return newState;
        default:
            return state;
    }
};

export default sessionReducer;
